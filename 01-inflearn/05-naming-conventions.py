# 다양한 변수 선언방법
# 1. Camel Case
numberOfCollegeGraduates = 100
# 처음에는 소문자, 연결되는 단어는 대문자로 시작
# 주로 메소드를 선언할 때 사용

# 2. Pascal Case
NumberOfCollegeGraduates = 100
# 모든 단어의 첫글자는 대문자로 시작
# 주로 클래스를 선언할 때 사용

# 3. Snake Case
number_of_college_graduates = 100
# 모든 단어의 첫글자는 소문자로 시작하고 단어와 단어 사이를 언더바(_)로 이어준다
# 주로 변수를 선언할 때 사용